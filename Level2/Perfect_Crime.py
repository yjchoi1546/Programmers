# 문제 설명
# A도둑과 B도둑이 팀을 이루어 모든 물건을 훔치려고 합니다. 단, 각 도둑이 물건을 훔칠 때 남기는 흔적이 누적되면 경찰에 붙잡히기 때문에, 두 도둑 중 누구도 경찰에 붙잡히지 않도록 흔적을 최소화해야 합니다.

# 물건을 훔칠 때 조건은 아래와 같습니다.

# 물건 i를 훔칠 때,
# A도둑이 훔치면 info[i][0]개의 A에 대한 흔적을 남깁니다.
# B도둑이 훔치면 info[i][1]개의 B에 대한 흔적을 남깁니다.
# 각 물건에 대해 A도둑과 B도둑이 남기는 흔적의 개수는 1 이상 3 이하입니다.
# 경찰에 붙잡히는 조건은 아래와 같습니다.

# A도둑은 자신이 남긴 흔적의 누적 개수가 n개 이상이면 경찰에 붙잡힙니다.
# B도둑은 자신이 남긴 흔적의 누적 개수가 m개 이상이면 경찰에 붙잡힙니다.
# 각 물건을 훔칠 때 생기는 흔적에 대한 정보를 담은 2차원 정수 배열 info, A도둑이 경찰에 붙잡히는 최소 흔적 개수를 나타내는 정수 n, B도둑이 경찰에 붙잡히는 최소 흔적 개수를 나타내는 정수 m이 매개변수로 주어집니다. 두 도둑 모두 경찰에 붙잡히지 않도록 모든 물건을 훔쳤을 때, A도둑이 남긴 흔적의 누적 개수의 최솟값을 return 하도록 solution 함수를 완성해 주세요. 만약 어떠한 방법으로도 두 도둑 모두 경찰에 붙잡히지 않게 할 수 없다면 -1을 return해 주세요.

# 제한사항
# 1 ≤ info의 길이 ≤ 40
# info[i]는 물건 i를 훔칠 때 생기는 흔적의 개수를 나타내며, [A에 대한 흔적 개수, B에 대한 흔적 개수]의 형태입니다.
# 1 ≤ 흔적 개수 ≤ 3
# 1 ≤ n ≤ 120
# 1 ≤ m ≤ 120



def solution(info, n, m):
    # 아직 계산되지 않은 무한대 값 (도달할 수 없음을 의미)
    INF = float('inf')
    
    # dp[a] : A의 흔적이 a일 때, 가능한 B의 흔적의 최솟값
    # A의 흔적은 무조건 n 미만이어야 하므로, 배열 크기는 n으로 잡습니다.
    dp = [INF] * n
    
    # 처음 시작할 때는 물건을 하나도 안 훔쳤으므로 
    # A의 흔적 0일 때, B의 흔적도 0입니다.
    dp[0] = 0
    
    # 물건을 하나씩 꺼내보며 도둑질을 시작합니다.
    for trace_a, trace_b in info:
        # 이번 물건을 훔치고 난 후의 결과를 적을 '새로운 기록장'
        next_dp = [INF] * n
        
        # 기존 기록장(dp)에 적혀있는 모든 상황을 확인합니다.
        for a in range(n):
            # 도달할 수 없는 상황(INF)은 건너뜁니다.
            if dp[a] != INF:
                
                # 선택지 1: A가 훔치는 경우
                # A의 새로운 흔적이 n을 넘지 않아야만 경찰에 안 잡힙니다.
                if a + trace_a < n:
                    # 기존에 a + trace_a 상황에 도달했던 B의 흔적과, 
                    # 지금 도달한 B의 흔적 중 더 작은(안전한) 값을 기록합니다.
                    next_dp[a + trace_a] = min(next_dp[a + trace_a], dp[a])
                
                # 선택지 2: B가 훔치는 경우
                # B의 새로운 흔적이 m을 넘지 않아야만 경찰에 안 잡힙니다.
                if dp[a] + trace_b < m:
                    # A의 흔적은 그대로 a이고, B의 흔적만 늘어납니다.
                    next_dp[a] = min(next_dp[a], dp[a] + trace_b)
        
        # 물건 하나를 처리했으니, 기록장을 업데이트합니다.
        dp = next_dp
        
    # 모든 물건을 다 훔쳤습니다! 이제 결과를 확인해 볼까요?
    # A의 흔적(a)이 가장 작은 0부터 n-1까지 차례대로 살펴봅니다.
    for a in range(n):
        # 만약 B의 흔적도 m을 넘지 않고 무사하다면(INF가 아니라는 것은 m보다 작게 유지됐다는 뜻)
        if dp[a] < m:
            return a # 작은 것부터 찾았으므로 찾는 즉시 반환하면 최소값이 됩니다.
            
    # 위에서 찾지 못했다면, 어떤 방법으로도 둘 다 안 잡힐 수가 없다는 뜻입니다.
    return -1